package day5_btvn_bai2;

public class tinhLuong {
	
	public double luong(int luongcoban, double hesoluong)
	{
		return luongcoban * hesoluong;
	}
	public double luong(int luongcoban, double hesoluong, int namkinhnghiem)
	{
		return luongcoban * hesoluong * namkinhnghiem;
	}
	public double luong(int luongcoban, double hesoluong, int namkinhnghiem, double loai)
	{
		return luongcoban * hesoluong * namkinhnghiem * loai;
	}
}

